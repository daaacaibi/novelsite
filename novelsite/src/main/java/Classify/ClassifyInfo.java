package Classify;

public class ClassifyInfo {
	int ID;
	String Classify;
	int BID;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getClassify() {
		return Classify;
	}
	public void setClassify(String classify) {
		Classify = classify;
	}
	public int getBID() {
		return BID;
	}
	public void setBID(int bID) {
		BID = bID;
	}

}