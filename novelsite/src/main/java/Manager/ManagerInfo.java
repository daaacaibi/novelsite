package Manager;

public class ManagerInfo {
	String MName;
	int MID;
	String MPwd;
	public String getMName() {
		return MName;
	}
	public void setMName(String mName) {
		MName = mName;
	}
	public int getMID() {
		return MID;
	}
	public void setMID(int mID) {
		MID = mID;
	}
	public String getMPwd() {
		return MPwd;
	}
	public void setMPwd(String mPwd) {
		MPwd = mPwd;
	}
	
}
