package BookShelf;

public class BookShelfInfo {
	int ID;
	int BID;
	int ChapterID;
	int UID;
	int BookShelfStatus;
	

	public int getBookShelfStatus() {
		return BookShelfStatus;
	}
	public void setBookShelfStatus(int bookShelfStatus) {
		BookShelfStatus = bookShelfStatus;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getBID() {
		return BID;
	}
	public void setBID(int bID) {
		BID = bID;
	}
	public int getChapterID() {
		return ChapterID;
	}
	public void setChapterID(int chapterID) {
		ChapterID = chapterID;
	}
	public int getUID() {
		return UID;
	}
	public void setUID(int uID) {
		UID = uID;
	}
	
}
